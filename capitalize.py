def capitalize():
    txt = 'ala ma kota'
    x = txt.capitalize()
    return x 
def count():
    txt = 'ala ma kota i kota'
    x = txt.count('kota')
    return x
def islower():
    txt = 'ALA MA KOTA'
    x = txt.islower()
    return x 
def isdigit():
    txt = 'ala ma kota 13'
    x = txt.isdigit
    return x 
def isalpha():
    txt = 'ala ma kota'
    x = txt.isalpha()
    return x 